def build_workflow(payload):
    prompt = payload.get("prompt", "")
    recipe = payload.get("recipe") or infer_recipe(prompt)
    media_type = normalize_media_type(payload.get("media_type"), recipe)
    quality = str(payload.get("quality") or "balanced").lower()
    if media_type == "image" and recipe not in {"flux_lora", "flux_base"}:
        recipe = "flux_lora"
    elif media_type == "video" and recipe in {"flux_lora", "flux_base"}:
        recipe = "wan_t2v"
    name = slugify(payload.get("name") or f"Assistant {recipe} {int(time.time())}")
    if recipe == "flux_base":
        wf, family = flux_workflow(prompt, with_lora=False, title=name, quality=quality)
    elif recipe == "flux_lora":
        strength = float(payload.get("lora_strength") or 0.75)
        wf, family = flux_workflow(
            prompt,
            lora=payload.get("lora") or None,
            loras=payload.get("loras"),
            strength=strength,
            with_lora=True,
            title=name,
            quality=quality,
        )
    else:
        wf, family = video_workflow(
            prompt,
            recipe=recipe,
            loras=payload.get("loras"),
            name=name,
            quality=quality,
        )
    return wf, family, name


def save_workflow(wf, family, name):
    target_dir = DESKTOP_WORKFLOWS / family
    target_dir.mkdir(parents=True, exist_ok=True)
    path = target_dir / f"{name}.json"
    path.write_text(json.dumps(wf, indent=2), encoding="utf-8")

    COMFY_WORKFLOWS.mkdir(parents=True, exist_ok=True)
    link = COMFY_WORKFLOWS / path.name
    if link.exists() or link.is_symlink():
        if link.is_symlink() or link.resolve() == path:
            link.unlink()
        else:
            backup = link.with_suffix(link.suffix + f".backup-{int(time.time())}")
            link.rename(backup)
    link.symlink_to(path)
    return path, link


def comfy_status():
    try:
        with urlrequest.urlopen(f"{COMFY_API}/system_stats", timeout=3) as resp:
            return json.loads(resp.read().decode("utf-8"))
    except Exception as exc:
        return {"error": str(exc)}


class Handler(BaseHTTPRequestHandler):
    server_version = "ComfyAssistantBuilder/0.1"

    def end_headers(self):
        origin = self.headers.get("Origin", "")
        if origin.startswith("chrome-extension://") or origin in {
            f"http://{HOST}:{PORT}",
            f"http://localhost:{PORT}",
            "http://127.0.0.1:8188",
            "http://localhost:8188",
        }:
            self.send_header("Access-Control-Allow-Origin", origin)
        self.send_header("Access-Control-Allow-Methods", "GET, POST, OPTIONS")
        self.send_header("Access-Control-Allow-Headers", "Content-Type, X-CAB-Token")
        self.send_header("X-Content-Type-Options", "nosniff")
        super().end_headers()

    def do_OPTIONS(self):
        self.send_response(204)
        self.end_headers()

    def do_GET(self):
        parsed = urlparse(self.path)
        if parsed.path == "/api/models":
            if not self.authorized():
                return self.json({"ok": False, "error": "unauthorized"}, code=401)
            return self.json(scan_models())
        if parsed.path == "/api/status":
            if not self.authorized():
                return self.json({"ok": False, "error": "unauthorized"}, code=401)
            return self.json({"ok": True, "comfy": comfy_status()})
        if parsed.path == "/api/lora-info":
            if not self.authorized():
                return self.json({"ok": False, "error": "unauthorized"}, code=401)
            name = parse_qs(parsed.query).get("name", [""])[0]
            return self.json(lora_info(name))
        if parsed.path == "/config.js":
            raw = f"window.CAB_TOKEN = {json.dumps(API_TOKEN)};\n".encode("utf-8")
            self.send_response(200)
            self.send_header("Content-Type", "application/javascript")
            self.send_header("Cache-Control", "no-store")
            self.send_header("Content-Length", str(len(raw)))
            self.end_headers()
            self.wfile.write(raw)
            return
        if parsed.path in ("/", "/index.html"):
            return self.file(STATIC_DIR / "index.html")
        path = STATIC_DIR / parsed.path.lstrip("/")
        if path.exists() and path.is_file():
            return self.file(path)
        self.send_error(404)

    def do_POST(self):
        parsed = urlparse(self.path)
        try:
            length = int(self.headers.get("Content-Length", "0"))
            payload = json.loads(self.rfile.read(length).decode("utf-8") or "{}")
            if parsed.path == "/api/build":
                if not self.authorized(payload):
                    return self.json({"ok": False, "error": "unauthorized"}, code=401)
                wf, family, name = build_workflow(payload)
                saved = None
                link = None
                if payload.get("save", True):
                    saved, link = save_workflow(wf, family, name)
                return self.json(
                    {
                        "ok": True,
                        "family": family,
                        "name": name,
                        "workflow": wf,
                        "saved_path": str(saved) if saved else None,
                        "comfy_link": str(link) if link else None,
                    }
                )
            self.send_error(404)
        except Exception as exc:
            self.json({"ok": False, "error": str(exc)}, code=500)

    def authorized(self, payload=None):
        header = self.headers.get("X-CAB-Token", "")
        return secrets.compare_digest(header, API_TOKEN)

    def json(self, data, code=200):
        raw = json.dumps(data, indent=2).encode("utf-8")
        self.send_response(code)
        self.send_header("Content-Type", "application/json")
        self.send_header("Content-Length", str(len(raw)))
        self.end_headers()
        self.wfile.write(raw)

    def file(self, path: Path):
        raw = path.read_bytes()
        self.send_response(200)
        self.send_header("Content-Type", mimetypes.guess_type(str(path))[0] or "application/octet-stream")
        self.send_header("Content-Length", str(len(raw)))
        self.end_headers()
        self.wfile.write(raw)

    def log_message(self, fmt, *args):
        print(f"[{self.log_date_time_string()}] {fmt % args}")


