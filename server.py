def lora_path(name: str) -> Path:
    root = MODEL_DIRS["loras"].resolve()
    path = (root / str(name or "")).resolve()
    if root != path and root not in path.parents:
        raise ValueError("LoRA path is outside the allowed folder")
    if not path.exists() or not path.is_file():
        raise FileNotFoundError("LoRA was not found")
    return path


def read_safetensors_metadata(path: Path):
    if path.suffix.lower() != ".safetensors":
        return {}
    with path.open("rb") as fh:
        raw_len = fh.read(8)
        if len(raw_len) != 8:
            return {}
        header_len = struct.unpack("<Q", raw_len)[0]
        if header_len <= 0 or header_len > 50_000_000:
            return {}
        header = json.loads(fh.read(header_len).decode("utf-8"))
    return header.get("__metadata__", {}) or {}


def flatten_trigger_value(value):
    if value is None:
        return []
    if isinstance(value, list):
        return [str(item).strip() for item in value if str(item).strip()]
    if isinstance(value, dict):
        out = []
        for key, item in value.items():
            if isinstance(item, (int, float)) and item <= 0:
                continue
            out.append(str(key).strip())
        return [item for item in out if item]
    text = str(value).strip()
    if not text:
        return []
    try:
        parsed = json.loads(text)
    except Exception:
        parsed = None
    if parsed is not None and parsed is not value:
        return flatten_trigger_value(parsed)
    return [part.strip() for part in re.split(r"[,;\n]+", text) if part.strip()]


def extract_trigger_words(metadata):
    direct_keys = [
        "trigger_words",
        "trigger_word",
        "trained_words",
        "trainedWords",
        "activation text",
        "activation_text",
        "trigger_phrase",
        "modelspec.trigger_phrase",
        "ss_trigger_words",
    ]
    triggers = []
    sources = []
    for key in direct_keys:
        if key in metadata:
            values = flatten_trigger_value(metadata.get(key))
            if values:
                triggers.extend(values)
                sources.append(key)
    if not triggers and metadata.get("ss_tag_frequency"):
        values = flatten_trigger_value(metadata.get("ss_tag_frequency"))
        if values:
            triggers.extend(values[:12])
            sources.append("ss_tag_frequency")
    clean = []
    seen = set()
    for item in triggers:
        item = re.sub(r"\s+", " ", item).strip()
        item = re.sub(r"^\d+[_: -]+", "", item).strip()
        if not item:
            continue
        low = item.lower()
        if low in seen:
            continue
        seen.add(low)
        clean.append(item)
    return clean[:24], sources


def classify_lora(name: str, metadata=None):
    metadata = metadata or {}
    text = " ".join(
        [
            name,
            str(metadata.get("modelspec.architecture", "")),
            str(metadata.get("modelspec.implementation", "")),
            str(metadata.get("modelspec.title", "")),
            str(metadata.get("ss_base_model_version", "")),
            str(metadata.get("ss_sd_model_name", "")),
            str(metadata.get("ss_network_module", "")),
            str(metadata.get("ss_output_name", "")),
        ]
    ).lower()
    if "flux" in text:
        return "Flux"
    if "wan" in text:
        return "Wan"
    if "ltxv" in text or "ltx-video" in text or "pytorch_lora_weights" in text:
        return "LTXV"
    if "sdxl" in text or "xl" in text or "stable-diffusion-xl" in text:
        return "SDXL"
    if "sd1.5" in text or "sd15" in text or "stable-diffusion-v1" in text or "v1-5" in text:
        return "SD1.5"
    if "qwen" in text:
        return "Qwen"
    return "Unknown"


def lora_catalog(loras=None):
    root = MODEL_DIRS["loras"].resolve()
    out = []
    for name in loras if loras is not None else list_files(MODEL_DIRS["loras"]):
        try:
            path = lora_path(name)
            metadata = read_safetensors_metadata(path)
            triggers, sources = extract_trigger_words(metadata)
            family = classify_lora(name, metadata)
            out.append(
                {
                    "name": str(path.relative_to(root)),
                    "family": family,
                    "compatible_recipes": compatible_recipes_for_family(family),
                    "trigger_words": triggers,
                    "source_keys": sources,
                    "has_metadata": bool(metadata),
                }
            )
        except Exception as exc:
            out.append(
                {
                    "name": str(name),
                    "family": "Unreadable",
                    "compatible_recipes": [],
                    "trigger_words": [],
                    "source_keys": [],
                    "has_metadata": False,
                    "error": str(exc),
                }
            )
    return sorted(out, key=lambda item: (item["family"], item["name"].lower()))


def compatible_recipes_for_family(family: str):
    return {
        "Flux": ["flux_lora"],
        "Wan": ["wan_t2v"],
        "LTXV": ["ltxv_t2v"],
    }.get(family, [])


def lora_info(name: str):
    path = lora_path(name)
    metadata = read_safetensors_metadata(path)
    triggers, sources = extract_trigger_words(metadata)
    root = MODEL_DIRS["loras"].resolve()
    return {
        "ok": True,
        "name": str(path.relative_to(root)),
        "family": classify_lora(str(path.relative_to(root)), metadata),
        "compatible_recipes": compatible_recipes_for_family(classify_lora(str(path.relative_to(root)), metadata)),
        "trigger_words": triggers,
        "source_keys": sources,
        "metadata_keys": sorted(metadata.keys())[:80],
        "has_metadata": bool(metadata),
    }


def pick(items, contains, fallback=None):
    contains = [c.lower() for c in contains]
    for item in items:
        low = item.lower()
        if all(c in low for c in contains):
            return item
    return fallback or (items[0] if items else "")


def infer_recipe(text: str) -> str:
    low = text.lower()
    if "wan" in low or "video" in low:
        return "wan_t2v"
    if "ltx" in low:
        return "ltxv_t2v"
    if "no lora" in low or "base" in low:
        return "flux_base"
    return "flux_lora"


def normalize_media_type(value: str, recipe: str) -> str:
    text = str(value or "").strip().lower()
    if text in {"image", "video"}:
        return text
    return "video" if recipe in {"wan_t2v", "ltxv_t2v"} else "image"


def best_lora_for_prompt(prompt: str, loras):
    low = prompt.lower()
    priority = []
    if "kwingo" in low or "wingo" in low:
        priority.append("kwingo")
    if "tracy" in low:
        priority.append("tracy_flux.1-dev_r16_final")
    if "real" in low:
        priority.append("flux_realism")
    priority.extend(["kwingo", "tracy_flux.1-dev_r16_final", "flux_realism", "flux-lora"])
    for token in priority:
        for lora in loras:
            if token in lora.lower():
                return lora
    return loras[0] if loras else ""


def node(
    nid,
    ntype,
    pos,
    size,
    order,
    inputs=None,
    outputs=None,
    widgets=None,
    title=None,
    properties=None,
    color=None,
    bgcolor=None,
):
    out = {
        "id": nid,
        "type": ntype,
        "pos": pos,
        "size": size,
        "flags": {},
        "order": order,
        "mode": 0,
        "inputs": inputs or [],
        "outputs": outputs or [],
        "properties": properties if properties is not None else {"Node name for S&R": ntype},
    }
    if widgets is not None:
        out["widgets_values"] = widgets
    if title:
        out["title"] = title
    if color:
        out["color"] = color
    if bgcolor:
        out["bgcolor"] = bgcolor
    return out


def out_slot(name, typ, links):
    return {"name": name, "type": typ, "slot_index": 0, "links": links}


def in_slot(name, typ, link):
    return {"name": name, "type": typ, "link": link}


def ensure_lora_compatible(name: str, recipe: str):
    info = lora_info(name)
    if recipe not in info["compatible_recipes"]:
        family = info.get("family", "Unknown")
        expected = recipe_family(recipe)
        raise ValueError(f"{name} is tagged as {family}, not compatible with the {expected} recipe")
    return info


def normalize_lora_slots(prompt, payload_loras, fallback_lora=None, fallback_strength=0.75, recipe="flux_lora"):
    slots = []
    if isinstance(payload_loras, list):
        for item in payload_loras:
            if not isinstance(item, dict):
                continue
            name = str(item.get("name") or "").strip()
            if not name:
                continue
            try:
                strength = float(item.get("strength", fallback_strength))
            except (TypeError, ValueError):
                strength = fallback_strength
            ensure_lora_compatible(name, recipe)
            slots.append({"name": name, "strength": strength})
    elif fallback_lora:
        ensure_lora_compatible(fallback_lora, recipe)
        slots.append({"name": fallback_lora, "strength": float(fallback_strength)})
    return slots[:5]


def auto_lora_slots(prompt, recipe="flux_lora", fallback_strength=0.75):
    models = scan_models()
    slots = []
    if not slots:
        compatible = [
            item["name"]
            for item in models.get("lora_catalog", [])
            if recipe in item.get("compatible_recipes", [])
        ]
        best = best_lora_for_prompt(prompt, compatible)
        if best:
            slots.append({"name": best, "strength": float(fallback_strength)})
    return slots[:5]


